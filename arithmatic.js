//************************************************************************************************
// Arithmetic Operators
//************************************************************************************************
// Operator	Description
// +	      Addition
// -	      Subtraction
// *	      Multiplication
// **	      Exponentiation (ES2016)
// /	      Division
// %	      Modulus (Division Remainder)
// ++	      Increment
// --	      Decrement
//************************************************************************************************

//************************************************************************************************
// ADDITION
//************************************************************************************************

var a = 10;
var b = 5;
console.log("ADDITION "+a+" and "+b+" -> " ,a+b);

//************************************************************************************************
// SUBTRACTION
//************************************************************************************************

var a = 10;
var b = 5;
console.log("SUBTRACTION "+a+" and "+b+" -> " ,a-b);

//************************************************************************************************
// MULTIPLICATION
//************************************************************************************************

var a = 10;
var b = 5;
console.log("MULTIPLICATION "+a+" and "+b+" -> " ,a*b);

//************************************************************************************************
// Exponentiation
//************************************************************************************************

var a = 10;
var b = 5;
console.log("Exponentiation "+a+" and "+b+" -> " ,a**b);

//************************************************************************************************
// Division Quotion
//************************************************************************************************

var a = 10;
var b = 5;
console.log("Division will give Quotion"+a+" and "+b+" -> " ,a/b);

//************************************************************************************************
// Modulus Reminder
//************************************************************************************************

var a = 10;
var b = 5;
console.log("Modulus will give Reminder"+a+" and "+b+" -> " ,a%b);

//************************************************************************************************
// Increment
//************************************************************************************************

var a = 10;
var b = 5;
console.log("Increment "+a+" and "+b+" -> " ,a++);

//************************************************************************************************
// Decrement
//************************************************************************************************

var a = 10;
var b = 5;
console.log("Decrement "+a+" and "+b+" -> " ,a--);


